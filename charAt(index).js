let word = "JavaScript";
let char = word.charAt(0);
let char2 = word.charAt(4);
let newChar = char + char2

console.log(newChar); // "js"

//  Explanation:
//  1. charAt(index) returns the character at the given position.
//  2. Indexing starts at 0.
//  3. If the index is out of range, it returns an empty string.
